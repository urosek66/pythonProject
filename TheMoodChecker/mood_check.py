mood = input("How are you felling today: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate")
elif mood == "excited":
    print("Why are you so excited?")
elif mood == "relaxed":
    print("How can you be so relaxed?")
else:
    print("I don't recognize this mood")